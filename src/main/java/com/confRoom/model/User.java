package com.confRoom.model;

public class User {
	private int userId;
	private String name;
	
	
	public void setUserId(int userId) {
		this.userId = userId;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return this.name;
	}
	
	public int getUserId() {
		return this.userId;
	}
	
}
